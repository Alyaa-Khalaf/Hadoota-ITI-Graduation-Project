const seedDB = async () => {
  try {
    await mongoose.connect(process.env.MONGODB_URI)
    console.log('✅ MongoDB Connected')
    console.log(`🌱 إضافة ${documents.length} وثيقة للـ Knowledge Base...`)

    let added = 0
    let skipped = 0

    for (const doc of documents) {
      try {
        await addDocument(doc)
        console.log(`✅ اتضاف: ${doc.title}`)
        added++
        // Small delay to avoid rate limiting on embedding API
        await new Promise(r => setTimeout(r, 500))
      } catch (err) {
        console.warn(`⚠️  فشل إضافة "${doc.title}": ${err.message}`)
        skipped++
      }
    }

    console.log(`\n🎉 تم الانتهاء! أضيف: ${added} — فشل: ${skipped}`)
    process.exit(0)
  } catch (error) {
    console.error('❌ Error:', error)
    process.exit(1)
  }
}

seedDB()
